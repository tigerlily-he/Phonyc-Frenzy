var getRandomInt = function(min, max) {
    return Math.floor(Math.random() * (max - min + 1)) + min;
};

var getNRandomWords = function(wordArray, num) {
	var thisLevelWords = {
		 index : [],
		result : []
	}
   

	
    for(var n=0; n<num; n++){
             var proceed = false;
             while(proceed !== true){
                 var temp = getRandomInt(0, wordArray.length-1);
                 if(thisLevelWords.index.indexOf(temp) === -1){
                    thisLevelWords.index.push(wordArray[temp]);
                     thisLevelWords.result.push(
					 
					{
						 indexIndex: thisLevelWords.index.length-1, 
						 vocab: wordArray[temp].translation.english
						});
                     thisLevelWords.result.push(
					 {
						 indexIndex: thisLevelWords.index.length-1,
						 vocab: wordArray[temp].translation.spanish
						});
                     proceed = true;
                 }
             }           
     }

     num = num * 2;
     while(num !== 0) {
        //pick a random int between 0 - 12
        //this is equal to the remaining words inside the array
        //after every word, we decrease the integer, so it is 0 - 11, 0 - 10, etc.
        var randomandajar=getRandomInt(0, num);
        num -= 1; 
        
        var temp = thisLevelWords.result[randomandajar];
        thisLevelWords.result[randomandajar] = thisLevelWords.result[num];
        thisLevelWords.result[num] = temp;
     }
     return thisLevelWords;
};

var easyWords= [guile, evade, feign, grim, exalt, fetid, filch, flair, flout, foil, forgo, glut, grim, guile, guru, hoard, impel, inane, inept, inert, iota, jest, jibe, lax, leery, limp, lull, lush, meek, ];

var mediumWords= [esteem, exacting, faction, fathom, fervent, forswear, fraught,fulminate, gratify, grievious, husband, immense, impassive, impasse, ingrained, innocuous, inundate, irascible, jaundiced, layman, litigious, magnify, manifest, marshal, menace, pristine];

var hardWords= [evanescent, exceptional, excruciating, fastidious, felicitous, formidable, fraudulent];


/** function for choosing N words
    params: level, num,
**/
var chooseWords= function(level, num) {
    var lays = [];
    //easy levels
     if(level>=1 && level<= 5){
        lays = getNRandomWords(easyWords, num);

     }
     else if(level>5 && level<11){
        lays = getNRandomWords(mediumWords, num);
     }
     else if (level>=11 && level<=15){
        lays = getNRandomWords(hardWords, num);
     }
     return lays;
};

